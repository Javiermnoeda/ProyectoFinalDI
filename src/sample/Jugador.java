package sample;

public class Jugador {
    public boolean esFavorito;
    public String nombre;
    public String equipo;
    public String posicion;
    public int dorsal;

    public Jugador (String nombre, boolean esFavorito, String equipo, String posicion, int dorsal){
        this.esFavorito = esFavorito;
        this.nombre = nombre;
        this.equipo = equipo;
        this.posicion = posicion;
        this.dorsal = dorsal;
    }


}
